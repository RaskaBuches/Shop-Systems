    void agregar() {
    // Obtener los valores de los campos de texto
    String nomUsuario = txtNomUsuario.getText();
    String password = String.valueOf(jPassContra.getPassword());
    String nombre = txtNombre.getText();
    String apellido = txtApellidos.getText();
    String fecha = txtFecha.getText();

    // Validar que los campos no est√©n vac√≠os
    if (nomUsuario.isEmpty() || password.isEmpty() || nombre.isEmpty() || apellido.isEmpty() || fecha.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return; // Salir del m√©todo si alg√∫n campo est√° vac√≠o
    }

    // Crear un arreglo de objetos con los datos del usuario
    Object[] ob = new Object[5];
    ob[0] = nomUsuario;
    ob[1] = password;
    ob[2] = nombre;
    ob[3] = apellido;
    ob[4] = fecha;

    // Llamar al m√©todo `add` del DAO para agregar el usuario
    dao.add(ob);

    // Mostrar un mensaje de confirmaci√≥n
    JOptionPane.showMessageDialog(this, "Usuario agregado correctamente");

    // Limpiar los campos de texto
    limpiarCampos();
}
    
    void limpiarCampos() {
    txtNomUsuario.setText("");
    jPassContra.setText("");
    txtNombre.setText("");
    txtApellidos.setText("");
    txtFecha.setText("");
}
