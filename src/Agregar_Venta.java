    void agregarProducto() {
    double total;
    int item = 0;
    modelo = (DefaultTableModel) tablaDetalle.getModel();
    item = modelo.getRowCount() + 1; // Obtener el n√∫mero de filas actuales y sumar 1
    try {
        int idp = Integer.parseInt(txtIdProd.getText());
        String nomp = txtNom.getText(); // Obtener el nombre del producto del campo correcto
        double precio = Double.parseDouble(txtPrecio.getText());
        int cant = Integer.parseInt(spinCantidad.getValue().toString());
        int stock = Integer.parseInt(txtStock.getText());

        total = cant * precio;
        if (stock > 0 && cant <= stock) { // Verificar si hay suficiente stock
            ArrayList<Object> lista = new ArrayList<>();
            lista.add(item);
            lista.add(idp);
            lista.add(nomp);
            lista.add(cant);
            lista.add(precio);
            lista.add(total);

            Object[] ob = new Object[6];
            for (int i = 0; i < ob.length; i++) {
                ob[i] = lista.get(i);
            }

            modelo.addRow(ob);
            tablaDetalle.setModel(modelo);
            calcularTotal();
        } else {
            JOptionPane.showMessageDialog(this, "Producto no Disponible o cantidad solicitada supera el stock");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Error en el formato de n√∫mero");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error inesperado");
    }
}

    
    void calcularTotal(){
        tpagar=0;
        for (int i = 0; i < tablaDetalle.getRowCount(); i++) {
            cant= Integer.parseInt(tablaDetalle.getValueAt(i, 3).toString());
            precio= Double.parseDouble(tablaDetalle.getValueAt(i, 4).toString());
            tpagar=tpagar+(cant*precio);
        }
        
        txtTotalP.setText(""+tpagar);
    }
    
    void buscarProducto() {
    String idProdText = txtIdProd.getText();
    
    if (idProdText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingresa Cod del Producto");
        return; // Salir del m√©todo si el campo de texto est√° vac√≠o
    }

    try {
        int id = Integer.parseInt(idProdText); // Intentar convertir el texto a entero
        Producto p = pdao.listarId(id);

        if (p != null && p.getId() != 0) {
            txtNom.setText(p.getNombre());
            txtPrecio.setText(String.valueOf(p.getPrecio()));
            txtStock.setText(String.valueOf(p.getStock()));
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese Codigo Valido");
            txtIdProd.requestFocus();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese un c√≥digo de producto v√°lido (n√∫mero entero)");
        txtIdProd.requestFocus();
    }
}
