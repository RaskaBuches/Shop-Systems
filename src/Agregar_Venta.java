    void actualizarStock(){
    for (int i = 0; i < modelo.getRowCount(); i++) {
        int idp = Integer.parseInt(tablaDetalle.getValueAt(i, 1).toString());
        int cant = Integer.parseInt(tablaDetalle.getValueAt(i, 3).toString());
        Producto p = pdao.listarId(idp); // Aseg√∫rate de que este m√©todo retorna un objeto Producto v√°lido
        int stockActualizado = p.getStock() - cant;
        pdao.actualizarStock(stockActualizado, idp);
        JOptionPane.showMessageDialog(this, "Venta Generada");
    }
}

