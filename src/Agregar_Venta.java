    void generarSerie(){
    String Serie = vdao.NroSerieVentas();
    
    if (Serie == null || Serie.isEmpty()) {
        txtSerieV.setText("00001");
    } else {
        int Mas = Integer.parseInt(Serie);
        Mas = Mas + 1;
        // Formatear el n√∫mero con ceros a la izquierda
        txtSerieV.setText(String.format("%05d", Mas));
    }
}
    
    private boolean hayProductosParaVender() {
    for (int i = 0; i < tablaDetalle.getRowCount(); i++) {
        int cantidad = (int) tablaDetalle.getValueAt(i, 3); // Aguarda la cantidad de productos en la pocision 3
        if (cantidad > 0) {
            return true;
        }
    }
    return false;
}

